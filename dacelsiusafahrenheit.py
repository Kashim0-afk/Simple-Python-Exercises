#Esercizio Da Celsius a Fahrenheit 06/12/2024


# Chiede la temperatura in gradi Celsius
celsius = float(input("Inserisci la temperatura in gradi Celsius: "))


# Formula di conversione
fahrenheit = (celsius * 9/5) + 32


