# Definizione della variabile iniziale
start_number = 30


# Operazione 1: Sottrazione
result1 = start_number - 25                      # 30 - 25 = 5
print("Risultato 1:", result1)


# Operazione 2: Divisione
result2 = start_number / 6                       # 30 / 6 = 5.0
print("Risultato 2:", result2)


# Operazione 3: Moltiplicazione e sottrazione
result3 = (start_number / 3) - 5                 # (30 / 3) - 5 = 5.0
print("Risultato 3:", result3)


# Operazione 4: Somma e sottrazione
result4 = (start_number - 28) + 3                # (30 - 28) + 3 = 5
print("Risultato 4:", result4)


# Operazione 5: divisione e sottrazione
result5 = (start_number / 5) - 1                 # (30 / 5) - 1 = 5.0
print("Risultato 5:", result5)
